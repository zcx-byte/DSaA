class StackCalculator {

    /**
     * Внутренний стек калькулятора.
     * Реализован как динамический массив ([MutableList]). Конец массива = вершина стека.
     *
     * При превышении внутренней ёмкости происходит аллокация нового массива и копирование
     * элементов (амортизационный анализ, метод кредитов из лекций).
     */
    private val stack = mutableListOf<Double>()

    /**
     * Универсальный обработчик бинарных операций.
     * Извлекает два операнда, применяет лямбду и возвращает результат в стек.
     *
     * **Временная сложность:** O(1) — две операции pop(), один вызов функции, один push().
     *
     * Демонстрирует принцип абстрагирования: общая логика вынесена, специфика задаётся параметром.
     *
     * @param operation функция (Double, Double) -> Double.
     */
    private fun binaryOp(operation: (Double, Double) -> Double) {
        val a = pop()
        val b = pop()
        push(operation(a, b))
    }

    /**
     * Помещает значение на вершину стека.
     *
     * **Временная сложность:** O(1) амортизированно. В худшем случае O(M) при realloc массива,
     * что компенсируется экспоненциальным ростом ёмкости и редкостью операции.
     *
     * @param value число с плавающей точкой для добавления в стек.
     */
    fun push(value: Double) {
        stack.add(value)
    }

    /**
     * Извлекает и возвращает верхний элемент стека, удаляя его из структуры.
     *
     * **Временная сложность:** O(1) — прямой доступ к индексу `size - 1`.
     
     *
     * Реализует свойство алгоритма "Конечность": явная проверка пустоты предотвращает
     * неопределённое поведение и бесконечные состояния.
     *
     * @return верхний элемент стека.
     * @throws IllegalStateException если стек пуст.
     */
    fun pop(): Double {
        if (stack.isEmpty()) throw IllegalStateException("Стек пуст: недостаточно операндов")
        return stack.removeLast()
    }


    /**
     * Сложение двух верхних элементов стека.
     * **Временная сложность:** O(1).
     */
    fun add() = binaryOp { a, b -> a + b }

    /**
     * Вычитание: (второй сверху) - (верхний элемент).
     * В RPN порядок операндов сохраняется при извлечении: первый pop() = правый операнд.
     * **Временная сложность:** O(1).
     */
    fun subtract() = binaryOp { a, b -> b - a }

    /**
     * Умножение двух верхних элементов.
     * **Временная сложность:** O(1).
     */
    fun multiply() = binaryOp { a, b -> a * b }

    /**
     * Деление: (второй сверху) / (верхний элемент).
     * **Временная сложность:** O(1).
     * @throws ArithmeticException при делении на ноль.
     */
    fun divide() = binaryOp { a, b ->
        if (a == 0.0) throw ArithmeticException("Деление на ноль")
        b / a
    }



    /**
     * Меняет местами два верхних элемента стека.
     * **Временная сложность:** O(1).
     * @throws IllegalStateException если в стеке менее 2 элементов.
     */
    fun swap() {
        if (stack.size < 2) throw IllegalStateException("Нужно минимум 2 элемента для обмена")
        val a = pop()
        val b = pop()
        push(a)
        push(b)
    }

    /**
     * Очищает стек, удаляя все элементы.
     * **Временная сложность:** O(1) — сброс внутреннего счётчика размера.
     */
    fun clear() = stack.clear()

    /**
     * Выводит текущее состояние стека в консоль.
     * **Временная сложность:** O(M) — копирование элементов во временный список для печати.
     * 
     */
    fun printStack() {
        if (stack.isEmpty()) {
            println("Стек: []")
        } else {
            println("Стек: ${stack.toList()}")
        }
    }

    /**
     * Обрабатывает строку ввода, разбивая её на токены и выполняя соответствующие команды.
     *
     * **Алгоритмический разбор:**
     * 1. Лексический анализ: разбиение по пробелам через регулярное выражение.
     * 2. Интерпретация: последовательная проверка токенов (`when`).
     * 3. Выполнение: вызов методов стека или безопасный парсинг чисел.
     *
     * @param input строка, содержащая числа и/или команды калькулятора, разделённые пробелами.
     */
    fun processInput(input: String) {

        // trim() удаляет пробелы по краям. split() разбивает по одному или более пробелам.
        // toRegex() компилирует строку в детерминированный конечный автомат (ДКА).
        val tokens = input.trim().split("\\s+".toRegex())

        for (token in tokens) {
            when {
                token.isBlank() -> continue
                token == "+" -> add()
                token == "-" -> subtract()
                token == "*" -> multiply()
                token == "/" -> divide()
                token == "swap" -> swap()
                token == "clear" -> clear()

                token == "print" -> printStack()
                // toDoubleOrNull() — безопасный парсер. Возвращает Double? или null.
                // После проверки != null компилятор Kotlin сужает тип до Double.
                token.toDoubleOrNull() != null -> push(token.toDouble())
                else -> println("Неизвестная команда: $token")
            }
        }
    }
}